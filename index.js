
function add(a, b) {
    return a + b
}
function subtract(a, b) {
    return a - b   
}
function multiply(a, b) {
    return a * b 
}
function divide(a, b) {
    return a / b
}

function increment(n="53"){
    n++
    return n
}
function decrement(n="678"){
    n--
    return n
}

function makeInt(n='126'){
    return parseInt(n)
}

function makeInt(n){
    return parseInt(n, 10)
}
function preserveDecimal(n){
    return parseFloat(n)
}







